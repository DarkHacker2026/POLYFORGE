def find_rtl_files(path: Path) -> list:
    """Find all Verilog/SystemVerilog/VHDL files in a path."""
    if path.is_file():
        return [path]
    files = []
    for ext in ["*.v", "*.sv", "*.vh", "*.svh", "*.vhd", "*.vhdl"]:
        files.extend(path.rglob(ext))
    return sorted(files)


def analyze_rtl_source(rtl_files: list) -> dict:
    """Search RTL source for real signals/modules suggesting the 4 capabilities.

    Returns a dict mapping capability name -> {found, evidence: [(file, line, text)]}.
    """
    results = {}
    for cap_name, cap_info in CAPABILITIES.items():
        cap_result = {
            "description": cap_info["description"],
            "found": False,
            "evidence": [],
        }

        for rtl_file in rtl_files:
            try:
                lines = rtl_file.read_text(encoding="utf-8", errors="replace").splitlines()
            except Exception:
                continue

            for i, line in enumerate(lines, 1):
                stripped = line.strip()
                # Skip comments
                if stripped.startswith("//") or stripped.startswith("/*") or stripped.startswith("--"):
                    continue

                for pattern in cap_info["search_patterns"]:
                    if re.search(pattern, stripped, re.IGNORECASE):
                        cap_result["found"] = True
                        cap_result["evidence"].append({
                            "file": str(rtl_file.name),
                            "line": i,
                            "text": stripped[:200],
                            "pattern_matched": pattern,
                        })
                        break  # one match per line is enough

        results[cap_name] = cap_result

    return results


def try_verilator_compile(rtl_files: list, wsl_rtl_path: str = None) -> dict:
    """Attempt to compile RTL with Verilator.

    Returns {success: bool, output: str, error: str}.
    """
    # Check if Verilator is available in WSL
    try:
        result = subprocess.run(
            ["wsl.exe", "-e", "bash", "-c",
             "export PATH=/home/dark_hacker/tools/verilator/bin:$PATH && "
             "verilator --version 2>&1"],
            capture_output=True, text=True, timeout=15
        )
        if result.returncode != 0 or "Verilator" not in result.stdout:
            return {
                "success": False,
                "output": "",
                "error": f"Verilator not available or broken: {result.stderr.strip()}",
            }
        verilator_version = result.stdout.strip()
    except Exception as e:
        return {
            "success": False,
            "output": "",
            "error": f"Cannot reach WSL/Verilator: {e}",
        }

    # Try to lint the first few RTL files
    # We use --lint-only which just checks syntax without needing a full testbench
    if not rtl_files:
        return {
            "success": False,
            "output": "",
            "error": "No RTL files found to compile.",
        }

    # Convert Windows paths to WSL paths for the first file
    first_file = rtl_files[0]
    win_path = str(first_file.absolute()).replace("\\", "/")
    wsl_path = win_path.replace("C:/", "/mnt/c/")

    try:
        result = subprocess.run(
            ["wsl.exe", "-e", "bash", "-c",
             f"export PATH=/home/dark_hacker/tools/verilator/bin:$PATH && "
             f"verilator --lint-only -Wno-fatal '{wsl_path}' 2>&1"],
            capture_output=True, text=True, timeout=30
        )
        output = result.stdout + result.stderr
        # Verilator lint-only returns 0 on success, non-zero on errors
        # But warnings are common and not necessarily fatal
        success = result.returncode == 0 or "Error:" not in output
        return {
            "success": success,
            "output": output[:3000],
            "error": "" if success else output[:1000],
            "verilator_version": verilator_version,
        }
    except subprocess.TimeoutExpired:
        return {
            "success": False,
            "output": "",
            "error": "Verilator lint timed out (30s).",
            "verilator_version": verilator_version,
        }
    except Exception as e:
        return {
            "success": False,
            "output": "",
            "error": f"Verilator execution failed: {e}",
            "verilator_version": verilator_version,
        }


def check_isa_spec(spec_path: Path) -> dict:
    """Check if an ISA spec file is provided and extract instruction encoding info.

    Returns {found: bool, content: str, opcodes: list}.
    """
    if not spec_path or not spec_path.exists():
        return {"found": False, "content": "", "opcodes": []}

    try:
        content = spec_path.read_text(encoding="utf-8", errors="replace")
    except Exception as e:
        return {"found": False, "content": "", "opcodes": [], "error": str(e)}

    # Search for opcode/instruction definitions
    opcodes = []
    # Look for patterns like: ADD, SUB, LW, SW, etc. in ISA docs
    for m in re.finditer(r'\b(?:opcode|instruction|inst)\s*[:=]\s*(\w+)', content, re.IGNORECASE):
        opcodes.append(m.group(1))
    # Look for register definitions
    reg_match = re.search(r'(\d+)\s*(?:registers|GPRs|regs)', content, re.IGNORECASE)
    num_regs = int(reg_match.group(1)) if reg_match else None

    return {
        "found": True,
        "content": content[:2000],
        "opcodes": opcodes[:20],
        "num_registers": num_regs,
    }


def report_capability_check(target_name: str, rtl_path: Path = None, isa_spec: Path = None):
    """Run the full capability check and print an honest report."""

    print(f"\n{'='*70}")
    print(f"  POLYFORGE RTL Capability Analysis")
    print(f"  Target: {target_name}")
    print(f"{'='*70}\n")

    # ─── PART 1: Accept RTL input and attempt simulation ────────────────────
    print("── PART 1: RTL Input and Simulation Attempt ──\n")

    if rtl_path and rtl_path.exists():
        rtl_files = find_rtl_files(rtl_path)
        print(f"  RTL source path: {rtl_path}")
        print(f"  RTL files found: {len(rtl_files)}")
        for f in rtl_files[:10]:
            print(f"    - {f.name}")
        if len(rtl_files) > 10:
            print(f"    ... and {len(rtl_files) - 10} more")

        if not rtl_files:
            print("\n  ❌ No Verilog/SystemVerilog/VHDL files found in the specified path.")
            print("  Cannot proceed with RTL analysis.")
            return

        # Try Verilator
        print(f"\n  Attempting Verilator lint-only compilation...")
        compile_result = try_verilator_compile(rtl_files)

        if compile_result.get("verilator_version"):
            print(f"  Verilator version: {compile_result['verilator_version']}")

        if compile_result["success"]:
            print(f"  ✅ Verilator lint succeeded (RTL is syntactically valid)")
            if compile_result.get("output"):
                # Show any warnings
                warnings = [l for l in compile_result["output"].splitlines() if "Warning" in l]
                if warnings:
                    print(f"  Warnings ({len(warnings)}):")
                    for w in warnings[:3]:
                        print(f"    {w.strip()[:150]}")
        else:
            print(f"  ❌ Verilator lint failed:")
            print(f"     {compile_result['error'][:300]}")
            print(f"\n  This RTL could not be compiled/linted with Verilator.")
            print(f"  Capability cannot be fully determined via simulation.")
            print(f"  Proceeding with source-level analysis only (Part 2)...")

        # ─── PART 2: Determine instruction-level capability ─────────────────
        print(f"\n── PART 2: Instruction-Level Capability Analysis ──\n")

        # Search RTL source for capability primitives
        print(f"  Searching {len(rtl_files)} RTL files for capability primitives...")
        cap_results = analyze_rtl_source(rtl_files)

        all_found = True
        for cap_name, cap_data in cap_results.items():
            status = "✅ FOUND" if cap_data["found"] else "❌ NOT FOUND"
            print(f"\n  [{cap_name}] {cap_data['description']}")
            print(f"    Status: {status}")
            if cap_data["evidence"]:
                print(f"    Evidence ({len(cap_data['evidence'])} matches):")
                for ev in cap_data["evidence"][:3]:
                    print(f"      {ev['file']}:{ev['line']}: {ev['text'][:120]}")
                if len(cap_data["evidence"]) > 3:
                    print(f"      ... and {len(cap_data['evidence']) - 3} more matches")
            if not cap_data["found"]:
                all_found = False

        # Check ISA spec if provided
        isa_result = check_isa_spec(isa_spec)
        if isa_result["found"]:
            print(f"\n  ISA specification found: {isa_spec}")
            print(f"    Opcodes discovered: {isa_result.get('opcodes', [])}")
            print(f"    Registers: {isa_result.get('num_registers', 'unknown')}")
        else:
            print(f"\n  No ISA specification provided.")
            print(f"  Instruction encoding must be inferred from RTL decode logic only.")

        # Determine if we can proceed
        missing = [name for name, data in cap_results.items() if not data["found"]]
        if missing:
            print(f"\n  ⚠️  Missing capabilities: {', '.join(missing)}")
            print(f"  This hardware LACKS primitives needed by the POLYFORGE abstraction.")
            print(f"  The pipeline cannot generate code for this target without these primitives.")
        else:
            print(f"\n  ✅ All 4 capabilities found in RTL source!")
            print(f"  This hardware APPEARS to have the primitives needed.")
            print(f"  NOTE: Source-level analysis is necessary but not sufficient.")
            print(f"  Full verification requires running code on the actual hardware.")

    elif target_name.lower() == "vortex":
        # Run the existing Vortex probes
        print("  Running existing Vortex hardware discovery probes...")
        print("  (These are the REAL probes that have been empirically verified.)\n")

        # Read the existing hardware facts
        facts_path = Path(__file__).resolve().parent.parent / "data" / "hardware_facts.vortex.json"
        if facts_path.exists():
            facts = json.loads(facts_path.read_text())
            simt = facts.get("simt_facts", {})
            print(f"  ┌─ Thread ID:          {'✅' if simt.get('thread_id_supported') else '❌'} "
                  f"(vx_thread_id() = {simt.get('thread_id_csr_value_on_main_thread', 'N/A')})")
            print(f"  ├─ Parallel Spawn:     ✅ (vx_spawn_threads, {simt.get('num_threads_per_warp', '?')} threads/warp)")
            print(f"  ├─ Barrier Sync:       {'✅' if simt.get('barrier_supported') else '❌'} "
                  f"({simt.get('barrier_primitive', 'N/A')})")
            print(f"  ├─ Addressable Memory: ✅ (LW/SW instructions, latency {facts.get('isa', {}).get('LW', {}).get('latency', '?')} cycles)")
            print(f"  └─ Total parallelism:  {simt.get('total_threads_per_core', '?')} threads/core")
            print(f"\n  Registers: {len(facts.get('registers', []))} GPRs")
            print(f"  ISA ops: {list(facts.get('isa', {}).keys())}")
            print(f"\n  ✅ Vortex: ALL capabilities verified empirically via rtlsim.")
            print(f"  This is the ONLY target with a proven end-to-end pipeline.")
        else:
            print("  ⚠️  No hardware_facts.vortex.json found. Run discovery_agent.py first.")

    else:
        # Unknown target with no RTL provided
        print(f"  No RTL source path provided for target '{target_name}'.")
        print(f"  No toolchain or simulator for '{target_name}' is available in this environment.")
        print(f"\n  ❌ Cannot empirically verify capability.")
        print(f"\n  A lowering/execution backend for '{target_name}' does not exist in this")
        print(f"  pipeline yet — it would need to be built, following the same")
        print(f"  discover → lower → verify pattern used for Vortex, but that is unbuilt")
        print(f"  and untested work, not a currently supported path.")
        print(f"\n  To analyze this target, provide RTL source with --rtl <path>")
        print(f"  and optionally an ISA spec with --isa-spec <path>.")

    print(f"\n{'='*70}")
    print(f"  Analysis complete.")
    print(f"{'='*70}\n")


def main():
    parser = argparse.ArgumentParser(
        description="POLYFORGE RTL Capability Analyzer",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Analyze Vortex RTL (regression test)
  python rtl_analyzer.py --target Vortex

  # Analyze arbitrary RTL
  python rtl_analyzer.py --rtl path/to/rtl/ --isa-spec path/to/isa.pdf

  # Check a named target with no backing (honest "cannot verify")
  python rtl_analyzer.py --target GenericGPU9000
        """,
    )
    parser.add_argument("--target", default=None,
                        help="Named target to check (e.g. 'Vortex', 'GenericGPU9000')")
    parser.add_argument("--rtl", default=None,
                        help="Path to RTL source directory or file")
    parser.add_argument("--isa-spec", default=None,
                        help="Path to ISA/instruction encoding specification")
    args = parser.parse_args()

    rtl_path = Path(args.rtl) if args.rtl else None
    isa_spec = Path(args.isa_spec) if args.isa_spec else None
    target = args.target or (rtl_path.name if rtl_path else "Unknown")

    report_capability_check(target, rtl_path, isa_spec)


if __name__ == "__main__":
    main()